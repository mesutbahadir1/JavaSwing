    //Yukarıda belirttiğim gibi kullanıcının girmiş olduğu tarih bilgisinin
    //doğruluğunu kontrol edip ona göre boolean true veya false dönen metoddur.
    public boolean tarihHesap(String gun, String ay, String yil) {
        boolean dogruTarih = true;
        int yil2 = Integer.parseInt(yil);
        int gun2 = Integer.parseInt(gun);

        switch (ay) {
            case "02":
                if (((yil2 % 4 == 0) && !(yil2 % 100 == 0)) || (yil2 % 400 == 0)) {
                    if (gun2 == 30 || gun2 == 31) {
                        dogruTarih = false;
                    } else {
                        dogruTarih = true;
                    }

                } else {
                    if (gun2 == 29 || gun2 == 30 || gun2 == 31) {
                        dogruTarih = false;
                    } else {
                        dogruTarih = true;
                    }

                }
                break;

            case "04":
                if (gun2 == 31) {
                    dogruTarih = false;
                } else {
                    dogruTarih = true;
                }
                break;

            case "06":
                if (gun2 == 31) {
                    dogruTarih = false;
                } else {
                    dogruTarih = true;
                }
                break;
            case "09":
                if (gun2 == 31) {
                    dogruTarih = false;
                } else {
                    dogruTarih = true;
                }
                break;

            case "11":
                if (gun2 == 31) {
                    dogruTarih = false;
                } else {
                    dogruTarih = true;
                }
                break;

        }
        return dogruTarih;
    }

