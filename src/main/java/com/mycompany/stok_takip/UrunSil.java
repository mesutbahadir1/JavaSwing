    private void btn_urunSilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_urunSilActionPerformed
        String id = "";
        String ad = "";
        String urunGrup = "";
        String fiyat="";
        String adetTotal = "";
        String tarih = "";
        //İlk başta textfield'ların dolu olup olmadığını kontrol ediyorum.
        if (txt_urunId.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Lütfen ürün id'sini giriniz.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            id = txt_urunId.getText();
        }

        String yil = cmb_yil.getSelectedItem().toString();
        String gun = cmb_gun.getSelectedItem().toString();
        String ay = cmb_ay.getSelectedItem().toString();
        //Aşağıda yazmış olduğum metod ile tarihin kontorlünü sağlıyorum. Akış dılı tarih girildiğinde
        //aşağıdaki hata mesajı verilir.(Örneğin 29.02.2017 olmayacağını gösterir.) 
        if (!(tarihHesap(gun, ay, yil))) {
            JOptionPane.showMessageDialog(null, "Hatalı tarih girişi yaptınız.", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        tarih = gun + "/" + ay + "/" + yil;

        try {
            //Database'e bağlanmak amacıyla burada bağlantılarımı açtım.
            connect = baglanti();
            state = connect.createStatement();
            result = state.executeQuery("SELECT * FROM app.urunler");
            int value = JOptionPane.showConfirmDialog(null, "Ürünü silmek istediğinize emin misiniz?", "Warning", JOptionPane.YES_NO_OPTION);
            if (value == JOptionPane.NO_OPTION) {
                return;
            }

            //Kayıt yaparken bağlandığım database'in kaç satırdan oluştuğunu öğrenmek için bu kısmı yazdım.
            int satirSayisi = 0;
            while (result.next()) {
                if (result.getString("URUNID") != null) {
                    satirSayisi++;
                } else {
                    break;
                }
            }

            connect.close();
            state.close();

            connect = baglanti();
            state = connect.createStatement();
            result = state.executeQuery("SELECT * FROM app.urunler");

            //Veritabanındaki satır sayısı 0'dan büyük olduğu ve ürünün id'si veritabanındaki id'ile eşleşirse
            //ürünü veritabanından siler. Veritabanındaki satır sayısı 0'sa yani içi boş ise güncellenecek
            //veri olmadığından henüz bir ürün girişi yapılmadığı uyarısı gelir. Eşleşen id yoksa uyarı verir.
            boolean isExist = false;
            if (satirSayisi > 0) {
                while (result.next()) {
                    ad = result.getString("URUNAD");
                    urunGrup = result.getString("URUNGRUBU");
                    adetTotal = result.getString("URUNADEDI");
                    fiyat=result.getString("URUNFİYAT");

                    String urunId = result.getString("URUNID");
                    if (id.equals(urunId)) {
                        //Bu kısımdaki query veritabanımdaki ürünler tablosundan ürünü silmeye yarar.
                        String query = "delete from app.urunler where urunId='" + id + "' ";
                        state.executeUpdate(query);
                        isExist = true;

                        //Connection'u close'layıp listenin tekrardan başından alınmasını sağlamış oluyorum.
                        connect.close();
                        state.close();

                        connect = baglanti();
                        state = connect.createStatement();

                        //Buradaki query ise veriler kısmında silinen ürünlerin verisini tutmak istediğim için
                        //veritabanındaki silinen ürünler tablosuna gerekli veriyi ekler.
                        String query2 = "insert into app.silinenurunler values('" + id + "',"
                                + "'" + ad + "','" + urunGrup + "','" + adetTotal + "','" + tarih + "','"+fiyat+"')";
                        state.executeUpdate(query2);
                        JOptionPane.showMessageDialog(null, "Ürün başarıyla silindi.", "Warning", JOptionPane.WARNING_MESSAGE);
                        txt_urunBulma.setText("");
                        txt_urunId.setText("");
                        cmb_ay.setSelectedIndex(0);
                        cmb_gun.setSelectedIndex(0);
                        cmb_yil.setSelectedIndex(0);
                        return;
                    }
                }
                if (!isExist) {
                    JOptionPane.showMessageDialog(null, "Girilen id'ye uygun ürün bulunamadı.", "Warning", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            } else {
                JOptionPane.showMessageDialog(null, "Henüz bir ürün girişi yapılmadı.", "Warning", JOptionPane.WARNING_MESSAGE);
                return;
            }

        } catch (SQLException ex) {
            Logger.getLogger(UrunGuncelle.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btn_urunSilActionPerformed

